          <p style="text-align: justify;"><a href="javaScript:expandabstract('SSM_cross_country')"><b>Legal recognition of 
          same-sex couples and family formation</b></a>. <b><em>Demography</em></b> 52:1 (February 2015), 113-151.</p>
          <div id="SSM_cross_country" style="display: none;">
            <p id="abstract">It has long been debated how legalizing same-sex marriage would impact (different-sex) family 
            formation. In this paper, I use data on OECD member countries for the period 1980–2009 to examine the effects 
            of the legal recognition of same-sex couples (through marriage or an alternative institution) on different-sex 
            marriage, divorce, and extramarital births. Estimates from difference-in-difference models indicate that the 
            introduction of same-sex marriage or of alternative institutions has no negative effects on family formation.
            These findings are robust to a multitude of specification checks, including the construction of counterfactuals
            using the synthetic control method. In addition, the country-by-country case studies provide evidence of
            homogeneity of the estimated effects.</p>
