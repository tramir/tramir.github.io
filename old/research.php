          <h3>Working papers</h3>

          <p style="text-align: justify;"><a href="javaScript:expandabstract('NL_Heterogeneity')"><b>Heterogeneous effects
          of medical interventions on the health of low-risk newborns</b></a> (with N. Meltem Daysal and Reyn van Ewijk), 
          November 2016 (an earlier version is available as IZA Discussion Paper No. 9810). <b>Submitted</b>.</p>
          <div id="NL_Heterogeneity" style="display: none;">
            <p id="abstract">We investigate heterogeneity in the impact of early-life medical interventions on low-risk 
            newborns' health. A policy rule in The Netherlands creates large discontinuities in medical treatments at 
            gestational week 37. Using a regression discontinuity design, we find no health benefits from additional 
            treatments for average newborns. However, there is substantial heterogeneity in returns to treatments, with 
            significant health benefits for newborns in the lowest income quartile and no benefits in higher income quartiles. 
            We provide suggestive evidence that potential difficulties in risk screening among low-income women cause this 
            heterogeneity. ``Back-of-the-envelope'' calculations suggest that providing additional treatments to all low-risk 
            births as per the current policy rule is barely efficient, but a targeted policy focusing on low-income areas would 
            be highly cost-effective.</p>
