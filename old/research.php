          <p style="text-align: justify;"><a href="javaScript:expandabstract('Marriage_Netherlands')"><b>The effect of 
          same-sex marriage laws on different-sex marriage: Evidence from the Netherlands</b></a>, <b><em>Demography</em></b> 
          51:1 (February 2014), 317-340. <span style="font-size: .85em; text-transform: uppercase;">Winner of the 2008
          Award for the Best Comparative Article Presented at an APPAM Research Conference.</span></p>
          <div id="Marriage_Netherlands" style="display: none;">
            <p id="abstract">It has long been argued that the legalization of same-sex marriage would have a negative impact
            on marriage. In this article, I examine how different-sex marriage in the Netherlands was affected by the 
            enactment of two laws: a 1998 law that provided all couples with an institution almost identical to marriage (a 
            "registered partnership") and a 2001 law that legalized same-sex marriage for the first time in the world. I 
            first construct a synthetic control for the Netherlands using OECD data for the period 1988–2005 and find that 
            neither law had significant effects on either the overall or different-sex marriage rate. I next construct a 
            unique individual-level data set covering the period 1995–2005 by combining the Dutch Labor Force Survey and 
            official municipal records. The estimates from a discrete-time hazard model with unobserved heterogeneity for
            the first-marriage decision confirm the findings in the aggregate analysis. The effects of the two laws are 
            heterogeneous, with presumably more-liberal individuals (as defined by their residence or ethnicity) marrying 
            less after passage of both laws and potentially more-conservative individuals marrying more after passage of 
            each law.</p>
