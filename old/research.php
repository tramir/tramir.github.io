          <p style="text-align: justify;"><a href="javaScript:expandabstract('BW_Spillovers')"><b>Spillover effects of
          early-life medical interventions</b></a> (with Sanni Breining, N. Meltem Daysal, and Marianne Simonsen), IZA
          Discussion Paper No. 9086. <b>Currently revising, new version coming soon</b>.</p>
          <div id="BW_Spillovers" style="display: none;">
            <p id="abstract">We investigate the spillover effects of early-life medical treatments on the siblings of
            treated children. We use a regression discontinuity design that exploits changes in medical treatments across
            the very low birth weight (VLBW) cutoff. Using administrative data from Denmark, we first confirm the findings
            in the previous literature that children who are slightly below the VLBW cutoff have better short- and long-term
            health, and higher math test scores in 9th grade. We next investigate spillover effects on siblings and find no
            evidence of an impact on their health outcomes. However, we find substantial positive spillovers on all our
            measures of academic achievement. Our estimates suggest that siblings of focal children who were slightly below
            the VLBW cutoff have higher 9th grade language and math test scores, as well as higher probability of enrolling
            in a high school by age 19. Our results suggest that improved interactions within the family may be an important
            pathway behind the observed spillover effects.</p>
