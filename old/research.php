          <p style="text-align: justify;"><a href="javaScript:expandabstract('Kenya')"><b>Banking sector stability, 
          efficiency, and outreach in Kenya</b></a> (with Thorsten Beck, Robert Cull, Michael J. Fuchs, Jared Getenga, 
          Peter K. Gatere, and John Randa), in Christopher Adam, Paul Collier, and Njuguna Ndung’u (eds.), <span 
          style="text-decoration: none; font-style:italic;">Kenya: Policies for Prosperity</span>, Oxford University Press, 
          2011. Also available as World Bank Policy Research Working Paper No. 5442, October 2010.</p>
          <div id="Kenya" style="display: none;">
            <p id="abstract">Although Kenya's financial system is by far the largest and most developed in East Africa and
            its stability has improved significantly over the past years, many challenges remain. This paper assesses the 
            stability, efficiency, and outreach of Kenya's banking system, using aggregate, bank-level, and survey data. 
            Banks' asset quality and liquidity positions have improved, making the system more resistant to shocks, and 
            interest rate spreads have declined, in part due to reduction in the overhead costs of foreign banks. Outreach 
            remains limited, but has improved in recent years, driven by mobile payments services in the domestic 
            remittance market. Fostering a level regulatory playing field for all deposit-taking institutions is a key 
            remaining challenge. Specifically, an effective but not overly burdensome framework for regulation and 
            supervision of microfinance institutions and cooperatives is a priority. Maintaining an openness to new, and 
            non-bank, providers of financial services, which has enabled the success of mobile payments, could also further 
            outreach.</p>
