          <p style="text-align: justify;"><a href="javaScript:expandabstract('NL_Homebirths')"><b>Saving lives at birth: 
          The impact of home births on infant outcomes</b></a> (with N. Meltem Daysal and Reyn van Ewijk). <b><em>American
          Economic Journal: Applied Economics</em></b> 7:3 (July 2015), 28-50.</p>
          <div id="NL_Homebirths" style="display: none;">
            <p id="abstract">Many developed countries have recently experienced sharp increases in home birth rates. This 
						paper investigates the impact of home births on the health of low-risk newborns using data from the Netherlands, 
						the only developed country where home births are widespread. To account for endogeneity in location of birth, 
						we exploit the exogenous variation in distance from a mother's residence to the closest hospital. We find that 
						giving birth in a hospital leads to substantial reductions in newborn mortality. We provide suggestive evidence 
						that proximity to medical technologies may be an important channel contributing to these health gains.</p>
