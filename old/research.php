          <p style="text-align: justify;"><a href="javaScript:expandabstract('PDEF')"><b>Quality of higher education and 
          the labor market in developing countries: Evidence from an education reform in Senegal</b></a> (with Dorothée 
          Boccanfuso and Alexandre Larouche). <b><em>World Development</em></b> 74 (October 2015), 412-424.</p>
          <div id="PDEF" style="display: none;">
            <p id="abstract">While many studies examine the effect of primary education quality on labor market outcomes 
            in developing countries, little is known about the effects at higher levels. We exploit the quasi-experiment 
            provided by a large-scale education reform launched in Senegal in 2000 to investigate how quality improvements 
            at the university level affect employment. Our difference-in-difference estimates suggest that young 
            high-skilled workers experienced a nine percentage-point employment gain relative to older workers. They are 
            also more likely to have &ldquo;better&rdquo; jobs (in the service industry or government), suggesting a 
            reduction in the mismatch between the quality of high-skill labor demanded and supplied.</p>
