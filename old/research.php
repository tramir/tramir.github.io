          <h3>RIP (abandoned projects)</h3>

          <p style="text-align: justify;"><a href="javaScript:expandabstract('NL_GYN')"><b>Returns to childbirth technologies: 
          Evidence from preterm births</b></a> (with N. Meltem Daysal and Reyn van Ewijk), IZA Discussion Paper No. 7834.</p>
          <div id="NL_GYN" style="display: none;">
            <p id="abstract">We investigate the impact of obstetrician supervision, as opposed to midwife supervision, on the 
            health of low-risk newborns. We exploit a unique policy rule in the Netherlands that creates a large discontinuity
            in the probability of a low-risk birth being attended by an obstetrician at gestational week 37. Using a fuzzy
            regression discontinuity design, we consistently find no health benefits from obstetrician supervision, despite 
            increased neonatal intensive care unit admission rates among births supervised by obstetricians. These results 
            indicate potential cost savings from shifting supervision of low-risk deliveries, which represent the vast majority 
            of all births, from obstetricians to midwives.</p>
